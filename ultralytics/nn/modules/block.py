class C2f_Ghost(nn.Module):
    def __init__(self, c1, c2, n=1, shortcut=False, ATT_TYPE='', k=3, g=1,
                 e=0.5):  # ch_in, ch_out, number, shortcut, groups, expansion
        super().__init__()
        self.c = int(c2 * e)  # hidden channels

        self.cv1 = Conv(c1, 2*self.c, 1, 1, None, g)  # down
        self.cv2 = Conv((2 + n) * self.c, c2, 1)  # optional act=FReLU(c2)
        self.m = nn.ModuleList(GhostBottleneck(self.c, self.c, k=k, s=1, shortcut=shortcut) for _ in range(n))
        if ATT_TYPE == 'EMA':
            self.ATT = EMA(c2)
        elif ATT_TYPE == 'CPCA':
            self.ATT = CPCA(c2, c2)
        else:
            self.ATT = None

    def forward(self, x):
        """Forward pass through C2f layer."""
        y = list(self.cv1(x).chunk(2, 1))  # [b,c,h,w]
        y.extend(m(y[-1]) for m in self.m)
        if self.ATT is None:
            return self.cv2(torch.cat(y, 1))
        else:
            return self.ATT(self.cv2(torch.cat(y, 1)))
