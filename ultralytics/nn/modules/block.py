class iBottleneck(nn.Module):
    def __init__(self, cin, cou, e=2, shortcut=True):
        super(iBottleneck, self).__init__()
        self.shortcut = shortcut
        self.c = int(cin * e)
        self.conv1 = Conv(cin, self.c, 1, 1)
        self.mixdwconv = MixDW(self.c, self.c)
        self.conv2 = Conv(self.c, cou, 1, 1)

    def forward(self, x):
        x1 = self.conv1(x)
        x2 = self.mixdwconv(x1)
        x3 = self.conv2(x2)
        return x + x3 if self.shortcut else x3


class GhostMobile(nn.Module):
    def __init__(self, cin, cou, n=1, shortcut=False):
        super(GhostMobile, self).__init__()  # def __init__(self, c1, c2, n=1, shortcut=False, g=1, e=0.5)
        self.c = int(cin / 2)
        self.reduce = Conv(cin, self.c, 1, 1)
        self.conv = Conv((1 + n) * self.c, cou, 1, 1)
        self.m = nn.ModuleList(iBottleneck(self.c, self.c, shortcut=shortcut) for _ in range(n))

    def forward(self, x):
        y = [self.reduce(x)]
        y.extend(m(y[-1]) for m in self.m)
        return self.conv(torch.cat(y, 1))


